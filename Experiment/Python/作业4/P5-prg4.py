s = [9, 7, 8, 3, 2, 1, 55, 6]
print("The number of elements in the list is", len(s))
print("The maximum value in this list is: ", max(s))
print("The minimum value in this list is: ", min(s))
print("The sum of the elements in the list is", sum(s))
print(f"The average of the elements in the list is {sum(s)/len(s):.2f}")