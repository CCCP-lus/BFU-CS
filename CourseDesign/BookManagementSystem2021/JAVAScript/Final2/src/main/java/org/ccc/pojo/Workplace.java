package org.ccc.pojo;

public class Workplace
{
    private int workplace_id;
    private String workplace_name;
    private String workplace_linkman;
    private String workplace_telephone;
    private String workplace_e_mail;
    private String workplace_address;
    private String workplace_character;

    public void setWorkplace_id(int workplace_id)
    {
        this.workplace_id = workplace_id;
    }

    public void setWorkplace_name(String workplace_name)
    {
        this.workplace_name = workplace_name;
    }

    public void setWorkplace_linkman(String workplace_linkman)
    {
        this.workplace_linkman = workplace_linkman;
    }

    public void setWorkplace_telephone(String workplace_telephone)
    {
        this.workplace_telephone = workplace_telephone;
    }

    public void setWorkplace_e_mail(String workplace_e_mail)
    {
        this.workplace_e_mail = workplace_e_mail;
    }

    public void setWorkplace_address(String workplace_address)
    {
        this.workplace_address = workplace_address;
    }

    public void setWorkplace_character(String workplace_character)
    {
        this.workplace_character = workplace_character;
    }

    public int getWorkplace_id()
    {
        return workplace_id;
    }

    public String getWorkplace_name()
    {
        return workplace_name;
    }

    public String getWorkplace_linkman()
    {
        return workplace_linkman;
    }

    public String getWorkplace_telephone()
    {
        return workplace_telephone;
    }

    public String getWorkplace_e_mail()
    {
        return workplace_e_mail;
    }

    public String getWorkplace_address()
    {
        return workplace_address;
    }

    public String getWorkplace_character()
    {
        return workplace_character;
    }
}
